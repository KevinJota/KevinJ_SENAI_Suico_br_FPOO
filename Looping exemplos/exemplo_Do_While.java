package setembro_14_loopings;

public class exemplo_Do_While {

	public static void main(String[] args) {
	
	int i = 0;
	
	do {  // esta chave inclui todo o processo a ser exibido primeiro antes da  condição do while
		
	System.out.println(i);
	i++;
		
		
		
	//caso condição do WHILE seja verdadeira irá repetir todo processo que está dentro das chaves de DO.	
	}while( i < 5);	//logo após fechar a chave deve-se colocar o while
		
		System.out.println("do while acabou");
	}

}
