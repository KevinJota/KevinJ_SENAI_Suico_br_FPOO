package setembro_14_loopings;

public class exemplo_par_impar_While {

	public static void main(String[] args) {
	
	int i = 0;	
	while( i < 10 )	{
		if (i % 2 == 0) {
		System.out.println("o "+i+" e numero par");
		} 
		else {
		System.out.println("o "+i+" e numero impar");
		}
	     i++;
	} 
			
		
	}

}
